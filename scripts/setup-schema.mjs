/**
 * setup-schema.mjs
 *
 * Creates the blogPost content model in Hygraph via the Management SDK.
 *
 * Prerequisites:
 *   1. In Hygraph → Project Settings → API Access → Permanent Auth Tokens
 *      create a token, give it schema write permissions, and add it to .env:
 *      HYGRAPH_MANAGEMENT_TOKEN=your_token_here
 *
 *   HYGRAPH_ENDPOINT is already in .env — no other URL needed.
 *
 * Install the SDK (one-time):
 *   npm install --save-dev @hygraph/management-sdk
 *
 * Run:
 *   node --env-file=.env scripts/setup-schema.mjs
 */

import { Client, SimpleFieldType } from "@hygraph/management-sdk";

const { HYGRAPH_MANAGEMENT_TOKEN, HYGRAPH_ENDPOINT } = process.env;

if (!HYGRAPH_MANAGEMENT_TOKEN) {
  console.error("Missing HYGRAPH_MANAGEMENT_TOKEN in .env");
  console.error(
    "  → Hygraph: Project Settings → API Access → Permanent Auth Tokens → create one with schema write permissions"
  );
  process.exit(1);
}

if (!HYGRAPH_ENDPOINT) {
  console.error("Missing HYGRAPH_ENDPOINT in .env");
  process.exit(1);
}

// The SDK expects the High Performance Content API URL (not CDN).
// If HYGRAPH_ENDPOINT is the CDN variant (cdn.hygraph.com/content/…),
// convert it to the standard API form (region.hygraph.com/v2/…).
function resolveApiEndpoint(raw) {
  // CDN format:  https://eu-west-2.cdn.hygraph.com/content/<id>/master
  // API format:  https://eu-west-2.hygraph.com/v2/<id>/master
  const cdnPattern =
    /^(https:\/\/)([\w-]+)\.cdn\.hygraph\.com\/content\/(.+)$/;
  const match = raw.match(cdnPattern);
  if (match) {
    const [, scheme, region, rest] = match;
    return `${scheme}${region}.hygraph.com/v2/${rest}`;
  }
  return raw; // already in API format or custom — use as-is
}

const apiEndpoint = resolveApiEndpoint(HYGRAPH_ENDPOINT);
console.log(`Using endpoint: ${apiEndpoint}`);

const client = new Client({
  authToken: HYGRAPH_MANAGEMENT_TOKEN,
  endpoint: apiEndpoint,
});

// 1. Create the BlogPost model
client.createModel({
  apiId: "BlogPost",
  apiIdPlural: "BlogPosts",
  displayName: "Blog Post",
});

// 2. blogTitle — single-line text, required, used as the display title
client.createSimpleField({
  parentApiId: "BlogPost",
  type: SimpleFieldType.String,
  apiId: "blogTitle",
  displayName: "Blog Title",
  isRequired: true,
  isTitle: true,
});

// 3. blogPostSlug — single-line text, required + unique (used in URLs)
client.createSimpleField({
  parentApiId: "BlogPost",
  type: SimpleFieldType.String,
  apiId: "blogPostSlug",
  displayName: "Blog Post Slug",
  isRequired: true,
  isUnique: true,
});

// 4. blogPostContent — rich text
client.createSimpleField({
  parentApiId: "BlogPost",
  type: SimpleFieldType.Richtext,
  apiId: "blogPostContent",
  displayName: "Blog Post Content",
});

// Submit all queued operations
console.log("Submitting schema migration…");
const result = await client.run();

if (result?.errors?.length) {
  console.error("Schema setup encountered errors:");
  result.errors.forEach((e) => console.error(" -", e.message ?? e));
  process.exit(1);
}

console.log("✓ BlogPost model created successfully.");
console.log(
  "  Fields: blogTitle, blogPostSlug, blogPostContent\n  (createdAt and createdBy are auto-generated by Hygraph)"
);
